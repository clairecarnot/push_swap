#include "push_swap.h"

void	rev_rotate_ab(t_list **a, t_list **b)
{
	if ((*a && (*a)->next) || (*b && (*b)->next))
		write(1, "rrr\n", 4);
	ft_rev_rotate(a);
	ft_rev_rotate(b);
}
